package com.syntax.homework;

public class HW02 {

	public static void main(String[] args) {
		
		for (int i=20; i>=1; i-=2) {
			System.out.print(i + " ");
		
}
		System.out.println("----------------");

		int d = 20;

		while (d <= 20 && d>=1) {

			if (d % 2 == 0) {
				System.out.print(d + " ");
				}
			d--;
		}
		System.out.println("----------------");

		int a = 50;

		while (a <= 50 && a>=20) {

			if (a % 2 != 0) {
				System.out.print(a + " ");
				}
			a--;
		}
		System.out.println("----------------");
	
		
		
		}
}
