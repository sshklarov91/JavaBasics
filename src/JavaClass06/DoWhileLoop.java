package JavaClass06;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		int num=10;
		while (num<20) {
			System.out.println(num + " ");
			num++;
		}
		System.out.println("--------------------");
		
		int num1=1;
		
		do {
			System.out.println("Hello World ");
			num1++; 
		
		} while(num1<6);
	}

}
