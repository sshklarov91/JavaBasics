package JavaClass06;

public class HelloFiveTimes {

	public static void main(String[] args) {
		
		for (int i=1; i<=5; i++) {
			System.out.println("Hello");
		}
		System.out.println("------------------------");
		
		for (int i=1; i<=20; i++) {
			System.out.print(i + " ");
		}
		
		System.out.println("------------------------");
		
		for (int i=30; i>=10; i--) {
			System.out.print(i + " ");
	}

		}
}